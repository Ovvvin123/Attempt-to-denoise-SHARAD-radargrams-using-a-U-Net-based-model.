# split_dataset.py
# 用途：
#   将 radar_ai_dataset 中的 .npz 数据按 track_id 划分为 train / val / test。
#
# 推荐运行：
#   F:\FInstallation\anaconda\python.exe split_dataset.py
#
# 输出：
#   outputs/splits/split.json
#   outputs/splits/train.txt
#   outputs/splits/val.txt
#   outputs/splits/test.txt
#   outputs/splits/split_summary.csv

from __future__ import annotations

import argparse
import csv
import json
import random
import re
from pathlib import Path
from typing import Dict, List, Any


def parse_args():
    parser = argparse.ArgumentParser(description="Split radar npz dataset into train/val/test.")

    parser.add_argument(
        "--data_dir",
        type=str,
        default="radar_ai_dataset",
        help="Folder containing *_dataset.npz files.",
    )

    parser.add_argument(
        "--stats_csv",
        type=str,
        default="outputs/stats/npz_stats.csv",
        help="CSV generated by check_npz_stats.py. If exists, only status == ok files will be used.",
    )

    parser.add_argument(
        "--out_dir",
        type=str,
        default="outputs/splits",
        help="Output folder for split files.",
    )

    parser.add_argument(
        "--train_ratio",
        type=float,
        default=0.70,
        help="Ratio of training data.",
    )

    parser.add_argument(
        "--val_ratio",
        type=float,
        default=0.15,
        help="Ratio of validation data.",
    )

    parser.add_argument(
        "--test_ratio",
        type=float,
        default=0.15,
        help="Ratio of test data.",
    )

    parser.add_argument(
        "--seed",
        type=int,
        default=2026,
        help="Random seed.",
    )

    return parser.parse_args()


def infer_track_id_from_name(filename: str) -> str:
    """
    从文件名中提取轨道编号。

    例如：
        S_00174302_dataset.npz -> S_00174302
        s_00174302_tiff.npz    -> S_00174302
        00174302_dataset.npz   -> S_00174302
    """
    stem = Path(filename).stem

    m = re.search(r"(S_\d{8})", stem, flags=re.IGNORECASE)
    if m:
        return m.group(1).upper()

    m = re.search(r"(\d{8})", stem)
    if m:
        return "S_" + m.group(1)

    # 如果实在提取不到，就退回文件名本身
    return stem


def clean_track_id(value: str | None, filename: str) -> str:
    """
    优先使用 stats_csv 里的 track_id。
    如果为空或者异常，则从文件名推断。
    """
    if value is None:
        return infer_track_id_from_name(filename)

    value = str(value).strip()

    if value == "" or value.lower() in ["nan", "none", "null"]:
        return infer_track_id_from_name(filename)

    m = re.search(r"(S_\d{8})", value, flags=re.IGNORECASE)
    if m:
        return m.group(1).upper()

    m = re.search(r"(\d{8})", value)
    if m:
        return "S_" + m.group(1)

    return value


def load_files_from_stats_csv(stats_csv: Path, data_dir: Path) -> List[Dict[str, Any]]:
    """
    从 check_npz_stats.py 输出的 npz_stats.csv 中读取 status == ok 的文件。
    """
    records = []

    with stats_csv.open("r", encoding="utf-8-sig", newline="") as f:
        reader = csv.DictReader(f)

        required_cols = {"file", "status"}
        missing_cols = required_cols - set(reader.fieldnames or [])

        if missing_cols:
            raise ValueError(f"{stats_csv} 缺少必要列：{missing_cols}")

        for row in reader:
            status = str(row.get("status", "")).strip()
            filename = str(row.get("file", "")).strip()

            if not filename:
                continue

            if status != "ok":
                continue

            file_path = data_dir / filename

            if not file_path.exists():
                print(f"[WARN] CSV 中存在但数据目录中找不到，跳过：{filename}")
                continue

            track_id = clean_track_id(row.get("track_id"), filename)

            width = row.get("width", "")
            height = row.get("height", "")
            mask_valid_ratio = row.get("mask_valid_ratio", "")

            records.append(
                {
                    "file": filename,
                    "track_id": track_id,
                    "height": height,
                    "width": width,
                    "mask_valid_ratio": mask_valid_ratio,
                    "source": "stats_csv",
                }
            )

    return records


def load_files_by_scanning(data_dir: Path) -> List[Dict[str, Any]]:
    """
    如果没有 npz_stats.csv，则直接扫描 data_dir 中的 .npz 文件。
    """
    npz_files = sorted(data_dir.glob("*.npz"))

    records = []

    for p in npz_files:
        filename = p.name
        track_id = infer_track_id_from_name(filename)

        records.append(
            {
                "file": filename,
                "track_id": track_id,
                "height": "",
                "width": "",
                "mask_valid_ratio": "",
                "source": "scan",
            }
        )

    return records


def group_by_track(records: List[Dict[str, Any]]) -> Dict[str, List[Dict[str, Any]]]:
    """
    将文件按 track_id 分组。
    同一个 track_id 的文件必须进入同一个集合，避免数据泄漏。
    """
    groups: Dict[str, List[Dict[str, Any]]] = {}

    for r in records:
        tid = r["track_id"]
        groups.setdefault(tid, []).append(r)

    return groups


def split_groups(
    groups: Dict[str, List[Dict[str, Any]]],
    train_ratio: float,
    val_ratio: float,
    test_ratio: float,
    seed: int,
):
    """
    按 track_id 分组划分 train / val / test。

    注意：
    - 划分单位是 track_id，不是单个 patch。
    - 如果一个 track_id 有多个 npz，它们会被放到同一个集合。
    """
    ratio_sum = train_ratio + val_ratio + test_ratio

    if ratio_sum <= 0:
        raise ValueError("train_ratio + val_ratio + test_ratio 必须大于 0")

    train_ratio = train_ratio / ratio_sum
    val_ratio = val_ratio / ratio_sum
    test_ratio = test_ratio / ratio_sum

    items = list(groups.items())

    rng = random.Random(seed)
    rng.shuffle(items)

    total_files = sum(len(v) for _, v in items)

    target_train = int(round(total_files * train_ratio))
    target_val = int(round(total_files * val_ratio))

    split = {
        "train": [],
        "val": [],
        "test": [],
    }

    train_count = 0
    val_count = 0

    for track_id, records in items:
        group_size = len(records)

        if train_count < target_train:
            split["train"].extend(records)
            train_count += group_size
        elif val_count < target_val:
            split["val"].extend(records)
            val_count += group_size
        else:
            split["test"].extend(records)

    # 防止极端情况下某个集合为空
    # 对于 3000 个样本一般不会触发
    if len(split["val"]) == 0 and len(split["train"]) > 1:
        split["val"].append(split["train"].pop())

    if len(split["test"]) == 0 and len(split["train"]) > 1:
        split["test"].append(split["train"].pop())

    return split


def check_no_track_leakage(split: Dict[str, List[Dict[str, Any]]]):
    """
    检查同一个 track_id 是否出现在多个集合中。
    """
    set_tracks = {}

    for split_name, records in split.items():
        tids = {r["track_id"] for r in records}
        set_tracks[split_name] = tids

    overlap_train_val = set_tracks["train"] & set_tracks["val"]
    overlap_train_test = set_tracks["train"] & set_tracks["test"]
    overlap_val_test = set_tracks["val"] & set_tracks["test"]

    leakage = {
        "train_val": sorted(list(overlap_train_val)),
        "train_test": sorted(list(overlap_train_test)),
        "val_test": sorted(list(overlap_val_test)),
    }

    has_leakage = any(len(v) > 0 for v in leakage.values())

    return has_leakage, leakage


def write_txt(path: Path, records: List[Dict[str, Any]]):
    """
    每行保存一个 npz 文件名。
    """
    with path.open("w", encoding="utf-8") as f:
        for r in records:
            f.write(r["file"] + "\n")


def write_summary_csv(path: Path, split: Dict[str, List[Dict[str, Any]]]):
    """
    保存每个文件所属集合，方便人工检查。
    """
    fieldnames = [
        "split",
        "file",
        "track_id",
        "height",
        "width",
        "mask_valid_ratio",
        "source",
    ]

    with path.open("w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()

        for split_name in ["train", "val", "test"]:
            for r in split[split_name]:
                row = {
                    "split": split_name,
                    "file": r.get("file", ""),
                    "track_id": r.get("track_id", ""),
                    "height": r.get("height", ""),
                    "width": r.get("width", ""),
                    "mask_valid_ratio": r.get("mask_valid_ratio", ""),
                    "source": r.get("source", ""),
                }
                writer.writerow(row)


def make_json_output(
    split: Dict[str, List[Dict[str, Any]]],
    args,
    used_source: str,
    leakage_info: Dict[str, List[str]],
):
    """
    生成 split.json 内容。
    """
    output = {
        "meta": {
            "data_dir": args.data_dir,
            "stats_csv": args.stats_csv,
            "used_source": used_source,
            "seed": args.seed,
            "train_ratio": args.train_ratio,
            "val_ratio": args.val_ratio,
            "test_ratio": args.test_ratio,
            "note": "Files are split by track_id to avoid leakage.",
        },
        "files": {
            "train": [r["file"] for r in split["train"]],
            "val": [r["file"] for r in split["val"]],
            "test": [r["file"] for r in split["test"]],
        },
        "tracks": {
            "train": sorted(list({r["track_id"] for r in split["train"]})),
            "val": sorted(list({r["track_id"] for r in split["val"]})),
            "test": sorted(list({r["track_id"] for r in split["test"]})),
        },
        "counts": {
            "num_train_files": len(split["train"]),
            "num_val_files": len(split["val"]),
            "num_test_files": len(split["test"]),
            "num_total_files": len(split["train"]) + len(split["val"]) + len(split["test"]),
            "num_train_tracks": len({r["track_id"] for r in split["train"]}),
            "num_val_tracks": len({r["track_id"] for r in split["val"]}),
            "num_test_tracks": len({r["track_id"] for r in split["test"]}),
        },
        "leakage_check": leakage_info,
    }

    return output


def main():
    args = parse_args()

    data_dir = Path(args.data_dir)
    stats_csv = Path(args.stats_csv)
    out_dir = Path(args.out_dir)

    if not data_dir.exists():
        raise FileNotFoundError(f"数据目录不存在：{data_dir.resolve()}")

    out_dir.mkdir(parents=True, exist_ok=True)

    if stats_csv.exists():
        print(f"[INFO] 使用 stats_csv 读取正常文件：{stats_csv.resolve()}")
        records = load_files_from_stats_csv(stats_csv, data_dir)
        used_source = "stats_csv_status_ok"
    else:
        print(f"[WARN] 没找到 stats_csv：{stats_csv.resolve()}")
        print("[WARN] 将直接扫描 data_dir 下所有 .npz 文件。")
        records = load_files_by_scanning(data_dir)
        used_source = "scan_all_npz"

    if len(records) == 0:
        raise RuntimeError("没有可用于划分的 npz 文件。请检查 radar_ai_dataset 或 npz_stats.csv。")

    groups = group_by_track(records)

    print("\n========== 数据读取结果 ==========")
    print(f"可用 npz 文件数：{len(records)}")
    print(f"track_id 数量：{len(groups)}")

    multi_file_tracks = {k: v for k, v in groups.items() if len(v) > 1}
    print(f"包含多个 npz 的 track_id 数量：{len(multi_file_tracks)}")

    split = split_groups(
        groups=groups,
        train_ratio=args.train_ratio,
        val_ratio=args.val_ratio,
        test_ratio=args.test_ratio,
        seed=args.seed,
    )

    has_leakage, leakage_info = check_no_track_leakage(split)

    if has_leakage:
        raise RuntimeError(f"发现 track_id 泄漏：{leakage_info}")

    split_json = make_json_output(
        split=split,
        args=args,
        used_source=used_source,
        leakage_info=leakage_info,
    )

    # 写出结果
    split_json_path = out_dir / "split.json"
    train_txt_path = out_dir / "train.txt"
    val_txt_path = out_dir / "val.txt"
    test_txt_path = out_dir / "test.txt"
    summary_csv_path = out_dir / "split_summary.csv"

    with split_json_path.open("w", encoding="utf-8") as f:
        json.dump(split_json, f, ensure_ascii=False, indent=2)

    write_txt(train_txt_path, split["train"])
    write_txt(val_txt_path, split["val"])
    write_txt(test_txt_path, split["test"])
    write_summary_csv(summary_csv_path, split)

    counts = split_json["counts"]

    print("\n========== 划分完成 ==========")
    print(f"Train 文件数：{counts['num_train_files']}，轨道数：{counts['num_train_tracks']}")
    print(f"Val   文件数：{counts['num_val_files']}，轨道数：{counts['num_val_tracks']}")
    print(f"Test  文件数：{counts['num_test_files']}，轨道数：{counts['num_test_tracks']}")
    print(f"Total 文件数：{counts['num_total_files']}")

    print("\n泄漏检查：通过")
    print("同一个 track_id 没有同时出现在 train / val / test 中。")

    print("\n输出文件：")
    print(f"split.json:        {split_json_path.resolve()}")
    print(f"train.txt:         {train_txt_path.resolve()}")
    print(f"val.txt:           {val_txt_path.resolve()}")
    print(f"test.txt:          {test_txt_path.resolve()}")
    print(f"split_summary.csv: {summary_csv_path.resolve()}")


if __name__ == "__main__":
    main()